                    <p><em><strong>História</strong></em></p>
                    <p>Ao pensarmos no que representaria a marca, nos veio em mente a ovelha de onde se extrai a lã, matéria prima muito utilizada na indústria têxtil. Porém, a sua representação como forma de animal, limitaria a utilização do símbolo para possíveis diversificações da marca. </p>
                    <p>Nisso criamos um símbolo composto por linhas, não deixando de representar a ideia original (ovelha -> lã -> fios de lã), como servindo para diversas aplicações futuras, além de carregar seus 4 atributos principais e possuir um aspecto atemporal.</p>
                    <p class="mt-5"><em><strong>Significado</strong></em></p>
                    <p>O nome xeep é uma referência ao nome em inglês para ovelha (sheep). As linhas representam o fio de lã.</p>
                    <p class="mt-5"><em><strong>Simbolismo</strong></em></p>
                    <p>A lã da ovelha é reconhecida mundialmente por ser uma das matérias primas mais utilizadas no mundo têxtil. Através da lã tratada criam-se fios e, com isso, a malha.</p>
                    <p>O símbolo é composto por uma esfera de borda ondulada que representa a lã da ovelha e dentro da esfera se encontra a letra X formada por quartro partes que representam as 4 caracteríscas da marca: forte, jovem, moderna e amiga.</p>
                    <p class="mt-5"><em><strong>Objeto</strong></em></p>
                    <p>Ovelha, novelo de lã, tecido.</strong></em></p>
                    <p class="mt-5"><em><strong>Atributos</strong></em></p>
                    <p>Força, juventude, modernidade e amizade.</p>
                    <label class="mb-3 mt-2">---</label>
                    <p class="texto-en">
                    <p><em><strong>History</strong></em></p>
                    <p>When thinking about what would represent the brand, the sheep, from which wool is extracted, a raw material widely used in the textile industry, came to mind. However, its representation as an animal would limit the use of the symbol for possible diversifications of the brand. </p>
                    <p>So, we created a symbol composed of lines, representing the original idea (sheep -> wool -> wool threads), as well as serving for several future applications, besides carrying its 4 main attributes and having a timeless aspect.</p>
                    <p class="mt-5"><em><strong>Meaning</strong></em></p>
                    <p>The name xeep is a reference to the English name for sheep. The lines represent the wool yarn.</p>
                    <p class="mt-5"><em><strong>Symbolism</strong></em></p>
                    <p>Sheep's wool is recognized worldwide for being one of the most used raw materials in the textile world. Treated wool is used to create yarn and thus knitwear.</p>
                    <p>The symbol is composed of a sphere with a wavy edge that represents wool, and inside the sphere is the letter X made up of four parts that represent the four characteristics of the brand: strong, young, modern, and friendly.</p>
                    <p class="mt-5"><em><strong>Object</strong></em></p>
                    <p>Sheep, ball of wool, cloth.</strong></em></p>
